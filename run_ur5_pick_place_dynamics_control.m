%% run_ur5_pick_place_dynamics_control.m
% UR5 full nonlinear dynamics simulation for a pick-and-place task.
%
% This script:
%   1. Builds a Cartesian pick-and-place task.
%   2. Uses utils/inverse_kinematics.m to generate a desired joint trajectory.
%   3. Simulates the full nonlinear UR5 dynamics using UR5_fdyn.
%   4. Applies a computed-torque / feedback-linearization tracking controller.
%   5. Adds an optional payload load while the object is being carried.
%   6. Plots tracking errors and control torques.
%   7. Animates the actual dynamic response and saves an MP4 video.
%
% Required repository files:
%   UR5_params.m
%   maple_gen/UR5_M.m
%   maple_gen/UR5_h.m
%   maple_gen/UR5_G.m
%   maple_gen/UR5_fdyn.m
%   maple_gen/UR5_fkine.m
%   maple_gen/UR5_fkall.m
%   maple_gen/UR5_jacobian_geometric.m
%   utils/inverse_kinematics.m


clear; clc; close all;

%% Paths

addpath('maple_gen');
addpath('utils');

assert(exist('UR5_params','file') == 2, 'UR5_params.m not found.');
assert(exist('UR5_M','file') == 2, 'UR5_M.m not found.');
assert(exist('UR5_h','file') == 2, 'UR5_h.m not found.');
assert(exist('UR5_G','file') == 2, 'UR5_G.m not found.');
assert(exist('UR5_fdyn','file') == 2, 'UR5_fdyn.m not found.');
assert(exist('UR5_fkine','file') == 2, 'UR5_fkine.m not found.');
assert(exist('UR5_fkall','file') == 2, 'UR5_fkall.m not found.');
assert(exist('UR5_jacobian_geometric','file') == 2, 'UR5_jacobian_geometric.m not found.');
assert(exist('inverse_kinematics','file') == 2, 'utils/inverse_kinematics.m not found.');

USE_FIG_HELPERS = (exist('figureoptscall','file') == 2) && ...
                  (exist('saveFigureAsPDF','file') == 2);

if USE_FIG_HELPERS
    figureoptscall;
end

%% Load UR5 parameters
[~, Pi] = UR5_params();
n = 6;

%% Output settings
outdir = 'ur5_pick_place_dynamics_results';
if ~exist(outdir, 'dir')
    mkdir(outdir);
end

videoFile = fullfile(outdir, 'ur5_pick_place_dynamics_control.mp4');
fps = 30;

%% IK settings for generating the reference trajectory
ikOpts = struct();
ikOpts.maxIter = 120;
ikOpts.positionTolerance = 2e-4;
ikOpts.lambda = 0.035;
ikOpts.alpha = 0.9;
ikOpts.maxStep = deg2rad(4);
ikOpts.useOrientation = false;

%% Dynamic simulation and controller settings
% Computed-torque controller:
%   tau_motor = M(q)*(ddq_d + Kd*(dq_d-dq) + Kp*(q_d-q)) + h(q,dq) + G(q)

Kp = diag([95, 95, 75, 55, 35, 25]);
Kd = diag([22, 22, 18, 14, 10, 8]);

useTorqueLimits = false;
tauLimit = [180; 180; 140; 90; 70; 50];   % Nm, used only if useTorqueLimits = true

% Optional object payload during the carry phase.
% If payload.compensate = true, the controller cancels the known payload
% force, so computed-torque tracking should remain close to exact.
% If payload.compensate = false, the payload acts as an unmodeled
% disturbance and a small tracking offset can appear during carrying.
payload.enable = true;
payload.compensate = true;
payload.mass = 0.35;          % kg
payload.gravity = 9.81;       % m/s^2

odeOpts = odeset( ...
    'RelTol', 1e-6, ...
    'AbsTol', 1e-8, ...
    'MaxStep', 1/(5*fps));

%% Define reachable task locations from known UR5 configurations
% These seed configurations define reachable Cartesian pick/place points.
% The actual reference trajectory is generated by numerical IK.

qHome = deg2rad([  30;  -80;  105; -115;  -90;    0]);
qPickSeed = deg2rad([  45;  -95;  115; -110;  -90;   20]);
qPlaceSeed = deg2rad([ -35;  -80;  105; -115;  -90;  -20]);

THome = UR5_fkine(qHome, Pi);
TPick = UR5_fkine(qPickSeed, Pi);
TPlace = UR5_fkine(qPlaceSeed, Pi);

pHome = THome(1:3,4);
pPick = TPick(1:3,4);
pPlace = TPlace(1:3,4);

liftHeight = 0.14;
pPickAbove = pPick + [0; 0; liftHeight];
pPlaceAbove = pPlace + [0; 0; liftHeight];

%% Build smooth Cartesian task trajectory
% Object modes:
%   0 = object waiting at pick location
%   1 = object attached to the end-effector
%   2 = object placed at final location

segments = {
    pHome,       pPickAbove,   50, 0, 'Move above object';
    pPickAbove,  pPick,        30, 0, 'Lower to grasp';
    pPick,       pPickAbove,   35, 1, 'Lift object';
    pPickAbove,  pPlaceAbove,  65, 1, 'Carry object';
    pPlaceAbove, pPlace,       30, 1, 'Lower to place';
    pPlace,      pPlaceAbove,  35, 2, 'Release object';
    pPlaceAbove, pHome,        50, 2, 'Return home'
};

pDes = [];
objectMode = [];
phaseIndex = [];
phaseNames = strings(size(segments,1),1);

for i = 1:size(segments,1)
    pA = segments{i,1};
    pB = segments{i,2};
    nFrames = segments{i,3};
    mode = segments{i,4};
    phaseNames(i) = string(segments{i,5});

    pts = smoothCartesianSegment(pA, pB, nFrames);

    if i > 1
        pts = pts(:,2:end); % Avoid duplicate frames at segment boundaries.
    end

    pDes = [pDes, pts]; %#ok<AGROW>
    objectMode = [objectMode, mode*ones(1,size(pts,2))]; %#ok<AGROW>
    phaseIndex = [phaseIndex, i*ones(1,size(pts,2))]; %#ok<AGROW>
end

nRef = size(pDes,2);
dtRef = 1/fps;
tRef = (0:nRef-1)*dtRef;
Tf = tRef(end);

%% Generate desired joint trajectory using numerical IK
Qref = zeros(n,nRef);
ikPositionError = zeros(1,nRef);
ikConverged = false(1,nRef);

qPrev = qHome;

fprintf('Generating IK reference for %d frames...\n', nRef);

for k = 1:nRef
    [qSol, info] = inverse_kinematics(pDes(:,k), qPrev, Pi, ikOpts);

    Qref(:,k) = qSol;
    qPrev = qSol;

    ikPositionError(k) = info.positionErrorNorm;
    ikConverged(k) = info.converged;
end

DQref = numericalDerivative(Qref, dtRef);
DDQref = numericalDerivative(DQref, dtRef);

fprintf('IK reference generated.\n');
fprintf('Maximum IK position error: %.6f m\n', max(ikPositionError));
fprintf('Mean IK position error   : %.6f m\n', mean(ikPositionError));
fprintf('Converged IK frames      : %d / %d\n', nnz(ikConverged), nRef);

%% Simulate full nonlinear dynamics
% Use exact initial conditions by default so the computed-torque controller
% demonstrates near-exact tracking for the nominal model.
%
% Set initialMismatch.enable = true if you want to intentionally show the
% transient response from a nonzero initial tracking error.

initialMismatch.enable = false;
initialMismatch.offset = deg2rad([2; -2; 1.5; -1; 1; 0.5]);

if initialMismatch.enable
    q0 = Qref(:,1) + initialMismatch.offset;
else
    q0 = Qref(:,1);
end

dq0 = DQref(:,1);
x0 = [q0; dq0];

dynData = struct();
dynData.Pi = Pi;
dynData.tRef = tRef;
dynData.Qref = Qref;
dynData.DQref = DQref;
dynData.DDQref = DDQref;
dynData.objectMode = objectMode;
dynData.Kp = Kp;
dynData.Kd = Kd;
dynData.payload = payload;
dynData.initialMismatch = initialMismatch;
dynData.useTorqueLimits = useTorqueLimits;
dynData.tauLimit = tauLimit;

fprintf('Simulating full nonlinear dynamics with computed-torque control...\n');

rhs = @(t,x) closedLoopDynamics(t, x, dynData);

% Passing tRef as the time vector returns the solution sampled exactly at
% the animation/reference frames.
[tSim, xSim] = ode45(rhs, tRef, x0, odeOpts);

fprintf('Dynamic simulation completed.\n');

%% Post-process simulation
sim = postprocessDynamics(tSim, xSim, dynData);

fprintf('Maximum joint tracking error: %.6f rad\n', max(sim.errNorm));
fprintf('Mean joint tracking error   : %.6f rad\n', mean(sim.errNorm));
fprintf('Peak motor torque norm      : %.6f Nm\n', max(sim.tauMotorNorm));
fprintf('Payload enabled             : %d\n', payload.enable);
fprintf('Payload compensated         : %d\n', payload.compensate);

save(fullfile(outdir, 'ur5_pick_place_dynamics_results.mat'), ...
    'sim', 'dynData', 'pDes', 'pPick', 'pPlace', 'pPickAbove', 'pPlaceAbove', ...
    'phaseIndex', 'phaseNames', 'ikPositionError', 'ikConverged');

%% Plot tracking results
fig1 = figure('Color','w', 'Name', 'UR5 Dynamics Tracking Summary', ...
    'Position', [100 100 1000 800]);

tl = tiledlayout(fig1, 2, 1, 'TileSpacing', 'compact', 'Padding', 'compact');

ax1 = nexttile(tl);
