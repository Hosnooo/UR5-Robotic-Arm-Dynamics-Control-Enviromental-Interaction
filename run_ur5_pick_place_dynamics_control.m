%% Local helper functions

function dx = closedLoopDynamics(t, x, data)
%CLOSEDLOOPDYNAMICS Full nonlinear UR5 dynamics under computed-torque control.

    q = x(1:6);
    dq = x(7:12);

    [qd, dqd, ddqd, mode] = referenceAtTime(t, data);

    e = qd - q;
    de = dqd - dq;

    v = ddqd + data.Kd*de + data.Kp*e;

    tauPayload = payloadGeneralizedTorque(q, mode, data);

    % Nominal computed-torque command for:
    %   M(q)*qdd + h(q,dq) + G(q) = tau
    tauNominal = UR5_M(q, data.Pi)*v + UR5_h(q, dq, data.Pi) + UR5_G(q, data.Pi);

    if data.payload.enable && data.payload.compensate
        % The plant receives tauMotor + tauPayload. Subtracting tauPayload
        % from the commanded motor torque cancels the known payload load.
        tauMotor = tauNominal - tauPayload;
    else
        % Payload, if enabled, remains as an external disturbance.
        tauMotor = tauNominal;
    end

    if data.useTorqueLimits
        tauMotor = min(max(tauMotor, -data.tauLimit), data.tauLimit);
    end

    % The plant sees motor torque plus external generalized torque.
    qdd = UR5_fdyn(q, dq, tauMotor + tauPayload, data.Pi);

    dx = [dq; qdd];
end

function sim = postprocessDynamics(tSim, xSim, data)
%POSTPROCESSDYNAMICS Evaluate references, errors, torques, and EE paths.

    t = tSim(:).';
    nT = numel(t);

    Q = xSim(:,1:6).';
    DQ = xSim(:,7:12).';

    Qref = zeros(6,nT);
    DQref = zeros(6,nT);
    DDQref = zeros(6,nT);
    err = zeros(6,nT);
    derr = zeros(6,nT);
    tauMotor = zeros(6,nT);
    tauPayload = zeros(6,nT);
    tauTotal = zeros(6,nT);
    modes = zeros(1,nT);
    eeActual = zeros(3,nT);
    eeDesired = zeros(3,nT);

    for k = 1:nT
        q = Q(:,k);
        dq = DQ(:,k);

        [qd, dqd, ddqd, mode] = referenceAtTime(t(k), data);

        e = qd - q;
        de = dqd - dq;

        v = ddqd + data.Kd*de + data.Kp*e;

        tauP = payloadGeneralizedTorque(q, mode, data);

        tauNominal = UR5_M(q, data.Pi)*v + UR5_h(q, dq, data.Pi) + UR5_G(q, data.Pi);

        if data.payload.enable && data.payload.compensate
            tauM = tauNominal - tauP;
        else
            tauM = tauNominal;
        end

        if data.useTorqueLimits
            tauM = min(max(tauM, -data.tauLimit), data.tauLimit);
        end

        Tactual = UR5_fkine(q, data.Pi);
        Tdesired = UR5_fkine(qd, data.Pi);

        Qref(:,k) = qd;
        DQref(:,k) = dqd;
        DDQref(:,k) = ddqd;
        err(:,k) = e;
        derr(:,k) = de;
        tauMotor(:,k) = tauM;
        tauPayload(:,k) = tauP;
        tauTotal(:,k) = tauM + tauP;
        modes(k) = mode;
        eeActual(:,k) = Tactual(1:3,4);
        eeDesired(:,k) = Tdesired(1:3,4);
    end

    sim = struct();
    sim.t = t;
    sim.Q = Q;
    sim.DQ = DQ;
    sim.Qref = Qref;
    sim.DQref = DQref;
    sim.DDQref = DDQref;
    sim.err = err;
    sim.derr = derr;
    sim.errNorm = vecnorm(err, 2, 1);
    sim.derrNorm = vecnorm(derr, 2, 1);
    sim.tauMotor = tauMotor;
    sim.tauPayload = tauPayload;
    sim.tauTotal = tauTotal;
    sim.tauMotorNorm = vecnorm(tauMotor, 2, 1);
    sim.tauPayloadNorm = vecnorm(tauPayload, 2, 1);
    sim.tauTotalNorm = vecnorm(tauTotal, 2, 1);
    sim.objectMode = modes;
    sim.eeActual = eeActual;
    sim.eeDesired = eeDesired;
end

function [qd, dqd, ddqd, mode] = referenceAtTime(t, data)
%REFERENCEATTIME Interpolate desired joint trajectory and object mode.

    tq = min(max(t, data.tRef(1)), data.tRef(end));

    qd = interp1(data.tRef, data.Qref.', tq, 'pchip').';
    dqd = interp1(data.tRef, data.DQref.', tq, 'pchip').';
    ddqd = interp1(data.tRef, data.DDQref.', tq, 'pchip').';

    mode = interp1(data.tRef, data.objectMode(:), tq, 'nearest', 'extrap');
end

function tauPayload = payloadGeneralizedTorque(q, mode, data)
%PAYLOADGENERALIZEDTORQUE Map payload weight to joint generalized torque.

    tauPayload = zeros(6,1);

    if ~data.payload.enable || mode ~= 1
        return;
    end

    [~, ~, Jg] = UR5_jacobian_geometric(q, data.Pi);
    Jv = Jg(1:3,:);

    Fpayload = [0; 0; -data.payload.mass * data.payload.gravity];

    tauPayload = Jv.' * Fpayload;
end

function dY = numericalDerivative(Y, dt)
%NUMERICALDERIVATIVE Central finite-difference derivative for row signals.

    dY = zeros(size(Y));

    dY(:,1) = (Y(:,2) - Y(:,1)) / dt;
    dY(:,end) = (Y(:,end) - Y(:,end-1)) / dt;

    if size(Y,2) > 2
        dY(:,2:end-1) = (Y(:,3:end) - Y(:,1:end-2)) / (2*dt);
    end
end

function pts = smoothCartesianSegment(pA, pB, nFrames)
%SMOOTHCARTESIANSEGMENT Quintic time-scaling between two 3D points.

    tau = linspace(0, 1, nFrames);
    s = 10*tau.^3 - 15*tau.^4 + 6*tau.^5;
    pts = pA(:) + (pB(:) - pA(:)) .* s;
end

